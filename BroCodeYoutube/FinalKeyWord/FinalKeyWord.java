package BroCodeYoutube.FinalKeyWord;

public class FinalKeyWord {
    // FINAL = ANYTHING THAT IS DECLARED AS FINAL CANNOT BE CHANGED OR UPDATED
    public static void main(String[] args) {
        final double pi = 3.14159;

        // pi = 4; ===> THIS LINE IS INVALID

        System.out.println(pi);
    }
}
