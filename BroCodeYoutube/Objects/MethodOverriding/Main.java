package BroCodeYoutube.Objects.MethodOverriding;

public class Main {
	// METHOD OVERRIDING = DECLARING A METHOD IN A SUB CLASS,
	//                     WHICH IS ALREADY PRESENT IN A PARENT CLASS.
	//                     IT IS DONE SO THAT A CHILD CLASS CAN GIVE ITS OWN IMPLEMENTATION
	public static void main(String[] args) {
		Dog dog = new Dog();
		dog.speak();
	}
	
}
