package BroCodeYoutube.WrapperClass;

public class Wrapper {
    // WRAPPER CLASS = PROVIDES A WAY TO USE PRIMITIVE DATA TYPES
    // AS REFERENCE DATA TYPES
    // REFERENCE DATA TYPE = HAS ACCESS TO USEFUL METHODS YOU CAN USE IN YOUR
    // PROGRAM

    // PRIMITIVE // WRAPPER
    // --------- // -------
    // boolean-- // Boolean
    // char----- // Character
    // int------ // Integer
    // double--- // Double

    public static void main(String[] args) {

        Boolean a = true;
        Character b = 's';
        Integer c = 1;
        Double d = 3.1;
        String e = "asd";

    }
}
